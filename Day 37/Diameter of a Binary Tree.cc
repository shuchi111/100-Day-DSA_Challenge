class Solution {
  public:
    // Function to return the diameter of a Binary Tree.
    int res=0;
    int diameter(Node* root) {
        int x = findRes(root);
        return res;
    }
    int findRes(Node *root){
        if(root == NULL) return 0;
        
        int lh = findRes(root->left);
        int rh = findRes(root->right);
        res = max(res , lh+rh+1);
        
        return 1 + max(lh,rh);
    }
};

